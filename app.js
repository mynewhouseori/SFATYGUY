function t(key) {
  return translations[currentLanguage][key] ?? translations.he[key] ?? key;
}

function localize(entry) {
  return entry[currentLanguage] ?? entry.he;
}

function getSelectedChecklistItem() {
  return mockData.checklist.find((item) => item.id === selectedChecklistId) ?? mockData.checklist[0];
}

function getStateLabel(state) {
  return t(`state${state.charAt(0).toUpperCase()}${state.slice(1)}`);
}

function getRiskLabel(risk) {
  if (risk === "danger") {
    return t("riskHigh");
  }

  if (risk === "warning") {
    return t("riskMedium");
  }

  return t("riskLow");
}

function openView(view) {
  currentView = view;
  navItems.forEach((item) => item.classList.toggle("is-active", item.dataset.viewTarget === view));
  views.forEach((section) => section.classList.toggle("is-active", section.dataset.view === view));
}

function setLanguage(language) {
  currentLanguage = translations[language] ? language : "he";
  const direction = directionMap[currentLanguage];

  document.documentElement.lang = direction.lang;
  document.documentElement.dir = direction.dir;

  languageButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === currentLanguage);
  });

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.dataset.i18n);
  });

  renderAll();
}

