function t(key) {
  return copy[key] ?? key;
}

function getSelectedChecklistItem() {
  return mockData.checklist.find((item) => item.id === selectedChecklistId) ?? mockData.checklist[0];
}

function getStateLabel(state) {
  return t(`state${state.charAt(0).toUpperCase()}${state.slice(1)}`);
}

function getRiskLabel(risk) {
  if (risk === "danger") {
    return t("riskHigh");
  }

  if (risk === "warning") {
    return t("riskMedium");
  }

  return t("riskLow");
}

function openView(view) {
  currentView = view;
  navItems.forEach((item) => item.classList.toggle("is-active", item.dataset.viewTarget === view));
  views.forEach((section) => section.classList.toggle("is-active", section.dataset.view === view));
}

function applyTextCopy() {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.dataset.i18n);
  });
}

