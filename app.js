document.addEventListener("click", (event) => {
  const cycleButton = event.target.closest("[data-action='cycle-state']");
  if (cycleButton) {
    const item = mockData.checklist.find((entry) => entry.id === cycleButton.dataset.id);
    item.state = stateCycle[item.state].next;
    selectedChecklistId = item.id;
    renderAll();
    return;
  }

  const checklistItem = event.target.closest(".check-item[data-id]");
  if (checklistItem) {
    selectedChecklistId = checklistItem.dataset.id;
    renderChecklist();
    renderIssuePreview();
    return;
  }

  const issueRow = event.target.closest(".issue-row[data-issue-id]");
  if (issueRow) {
    selectedChecklistId = issueRow.dataset.issueId;
    openView("home");
    renderAll();
  }
});

