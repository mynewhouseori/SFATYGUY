function renderCategorySummary() {
  const groups = [
    { id: "ppe", title: "ציוד מגן אישי" },
    { id: "height", title: "עבודה בגובה" },
    { id: "electrical", title: "חשמל זמני" },
    { id: "excavation", title: "עבודות חפירה" },
  ];

  categorySummary.innerHTML = groups
    .map((group) => {
      const items = mockData.checklist.filter((item) => item.category === group.id);
      const hasDanger = items.some((item) => item.state === "danger");
      const hasWarning = items.some((item) => item.state === "warning");
      const allOk = items.every((item) => item.state === "ok");
      const tone = hasDanger ? "danger" : hasWarning ? "warning" : allOk ? "ok" : "muted";
      const status = hasDanger ? getStateLabel("danger") : hasWarning ? `${items.filter((item) => item.state === "warning").length} בטיפול` : allOk ? getStateLabel("ok") : getStateLabel("muted");

      return `
        <div class="stack-row">
          <strong>${group.title}</strong>
          <span class="site-badge ${tone === "muted" ? "muted" : tone}">${status}</span>
        </div>
      `;
    })
    .join("");
}

